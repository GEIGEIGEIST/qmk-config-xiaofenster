bool encoder_update_user(uint8_t index, bool clockwise) {
    if (IS_LAYER_ON(_SYM)) {
        if (clockwise) {
            tap_code(KC_PPLS);
        } else {
            tap_code(KC_PMNS);
        }
    } else {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    return true;
}
